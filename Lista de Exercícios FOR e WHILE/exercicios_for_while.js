// 1 - Exiba os números de 1 a 10 utilizando while

let i = 1;
while (i <= 10) {
    console.log(i);
    i++;
}

// 2 - Exiba os números de 10 a 1 utilizando while

let j = 10;
while (j >= 1) {
    console.log(j);
    j--;
}

